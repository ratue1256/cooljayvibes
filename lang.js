const SUPPORTED_LANGS = Object.keys(TRANSLATIONS);
const LANG_LABELS = {
    en: "English", fr: "Français", de: "Deutsch", it: "Italiano", es: "Español",
    pt: "Português", nl: "Nederlands", sv: "Svenska", pl: "Polski", "zh-CN": "中文"
};

function detectLang() {
    let saved = localStorage.getItem('cooljay_lang');
    if (saved && SUPPORTED_LANGS.includes(saved)) return saved;
    const nav = navigator.language || 'en';
    const base = nav.toLowerCase();
    // Try exact match first (e.g. zh-CN)
    if (SUPPORTED_LANGS.includes(base)) return base;
    // Then base match (e.g. 'fr' from 'fr-FR')
    const short = base.split('-')[0];
    if (SUPPORTED_LANGS.includes(short)) return short;
    return 'en';
}

function applyTranslations(lang) {
    const t = TRANSLATIONS[lang] || TRANSLATIONS.en;
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key] !== undefined) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = t[key];
            } else {
                el.textContent = t[key];
            }
        }
    });
    // Update html lang attribute
    document.documentElement.lang = lang;
}

function buildLangMenu() {
    const container = document.getElementById('lang-menu-list');
    if (!container) return;
    const current = detectLang();
    container.innerHTML = '';
    SUPPORTED_LANGS.forEach(code => {
        const btn = document.createElement('button');
        btn.type = 'button';
        btn.dataset.lang = code;
        btn.className = 'lang-item w-full px-4 py-2.5 text-left text-sm flex items-center justify-between hover:bg-white/5 transition-colors ' + (code === current ? 'text-white font-semibold' : 'text-[#86868B]');
        btn.innerHTML = LANG_LABELS[code] + (code === current ? '<svg class="w-3 h-3 text-[#2997FF]" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="3"><polyline points="20 6 9 17 4 12"></polyline></svg>' : '');
        btn.addEventListener('click', (e) => {
            e.stopPropagation();
            localStorage.setItem('cooljay_lang', code);
            applyTranslations(code);
            // Update display
            const disp = document.getElementById('current-lang-display');
            if (disp) disp.textContent = code === 'zh-CN' ? 'ZH' : code.toUpperCase();
            buildLangMenu();
            // Close dropdown
            const dd = document.getElementById('lang-dropdown');
            if (dd) dd.classList.add('hidden');
        });
        container.appendChild(btn);
    });
}

document.addEventListener('DOMContentLoaded', () => {
    const lang = detectLang();
    localStorage.setItem('cooljay_lang', lang);
    applyTranslations(lang);
    buildLangMenu();

    // Display label
    const disp = document.getElementById('current-lang-display');
    if (disp) disp.textContent = lang === 'zh-CN' ? 'ZH' : lang.toUpperCase();

    // Toggle dropdown
    const langBtn = document.getElementById('lang-toggle-btn');
    const langDropdown = document.getElementById('lang-dropdown');
    if (langBtn && langDropdown) {
        langBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            langDropdown.classList.toggle('hidden');
        });
        document.addEventListener('click', () => langDropdown.classList.add('hidden'));
    }
});
